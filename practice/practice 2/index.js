let a = 10
let b = 5

console.log( "1.", "a + b =", a+b) //Addition
console.log( "2.", "a - b =", a-b) //Subtraction
console.log( "3.", "a * b =", a*b) //Multiplication
console.log( "4.", "a ** b =", a**b) //Exponention
console.log( "5.", "a / b =", a/b) //Division
console.log( "6.", "a % b =", a%b) //Modulus (also known as remainder)
console.log( "7.", "a++ =", a++) //Increament (Increament not-visible first and visible after in next equation)
console.log( "8.", "++a =", ++a) //Increament (Increament visible first and then in next equation)
console.log( "9.", "a-- =", a--)
console.log("value of a = ", a)
console.log("10.", "--a", --a)
